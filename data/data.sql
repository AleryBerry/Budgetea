PRAGMA foreign_keys = ON;
CREATE TABLE IF NOT EXISTS "account" (
	"id"	INTEGER NOT NULL,
	"name"	TEXT NOT NULL DEFAULT ('""'),
	"can_receive_cash_flows"	BOOLEAN NOT NULL CHECK("can_receive_cash_flows" IN (0, 1)),
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "account_relationship" (
	"id"	INTEGER NOT NULL,
	"parent_account"	INTEGER NOT NULL,
	"child_account"	INTEGER NOT NULL UNIQUE,
	PRIMARY KEY("id" AUTOINCREMENT),
	CONSTRAINT "account_relationship_account_FK_1" FOREIGN KEY("child_account") REFERENCES "account"("id") ON DELETE CASCADE ON UPDATE CASCADE,
	CONSTRAINT "account_relationship_account_FK" FOREIGN KEY("parent_account") REFERENCES "account"("id") ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "cash_flow" (
	"id"	INTEGER NOT NULL,
	"date"	TEXT NOT NULL,
	"amount"	NUMERIC NOT NULL,
	"account"	INTEGER NOT NULL,
	"currency"	INTEGER NOT NULL,
	"category"	INTEGER,
	"description"	TEXT NOT NULL DEFAULT '',
	"description_image"	BLOB,
	PRIMARY KEY("id" AUTOINCREMENT),
	FOREIGN KEY("account") REFERENCES "account"("id") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("category") REFERENCES "cash_flow_category"("id") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("currency") REFERENCES "currency"("id") ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "cash_flow_category" (
	"id"	INTEGER NOT NULL,
	"name"	TEXT NOT NULL,
	"icon_name"	TEXT,
	"icon_color"	TEXT,
	"icon_pack"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "currency" (
	"id"	INTEGER NOT NULL,
	"name"	TEXT NOT NULL,
	"symbol"	TEXT,
	"iso"	TEXT NOT NULL UNIQUE,
	"logo_url"	TEXT,
	"type"	TEXT NOT NULL DEFAULT 'FIAT' CHECK("type" IN ('FIAT', 'CRYPTO', 'STOCK')),
	"decimal_points"	INTEGER NOT NULL DEFAULT 2,
	"symbol_on_left"	INTEGER NOT NULL DEFAULT 1 CHECK(symbol_on_left IN (0,1)),
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "currency_pair" (
	id INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
	currency_origin INTEGER NOT NULL,
	currency_target INTEGER NOT NULL,
	CONSTRAINT currency_par_currency_FK FOREIGN KEY (currency_origin) REFERENCES currency(id) ON DELETE CASCADE ON UPDATE CASCADE,
	CONSTRAINT currency_par_currency_FK_1 FOREIGN KEY (currency_target) REFERENCES currency(id) ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "currency_pair_rate" (
	"id"	INTEGER NOT NULL,
	"date"	TEXT NOT NULL,
	"currency_pair"	INTEGER NOT NULL,
	"rate"	REAL NOT NULL,
	UNIQUE("date","currency_pair","rate"),
	PRIMARY KEY("id" AUTOINCREMENT),
	CONSTRAINT "currency_pair_price_currency_pair_FK" FOREIGN KEY("currency_pair") REFERENCES "currency_pair"("id") ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "transfer" (
	"id"	INTEGER NOT NULL UNIQUE,
	"cash_flow_origin"	INTEGER NOT NULL UNIQUE,
	"cash_flow_target"	INTEGER NOT NULL UNIQUE,
	PRIMARY KEY("id" AUTOINCREMENT),
	FOREIGN KEY("cash_flow_origin") REFERENCES "cash_flow"("id") ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY("cash_flow_target") REFERENCES "cash_flow"("id") ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE VIEW account_roots as
SELECT * FROM account as a
where a.id not in (select distinct child_account from account_relationship);
CREATE VIEW account_totals as
SELECT acc.id, acc.name, caf.currency, ifnull(sum(caf.amount), 0) as amount FROM account as acc
full join cash_flow as caf on caf.account = acc.id
group by acc.id, caf.currency;
CREATE TRIGGER Prevent_CashFlow_For_NonReceivingAccount
BEFORE INSERT ON cash_flow
FOR EACH ROW
WHEN NEW.account IS NOT NULL
BEGIN
    SELECT
        CASE
            WHEN (SELECT can_receive_cash_flows FROM account WHERE id = NEW.account) = 0
            THEN
                RAISE(FAIL, 'Cannot insert cash flow into this account: Account is configured to not receive cash flows.')
        END;
END;
CREATE TRIGGER Prevent_Cyclical_Account_Relationship
BEFORE INSERT ON Account_Relationship
FOR EACH ROW
WHEN NEW.parent_account IS NOT NULL AND NEW.child_account IS NOT NULL
BEGIN
    -- Initialize the path with the potential parent
    WITH PathToCheck(Ancestor) AS (
      SELECT NEW.parent_account
      UNION ALL
      SELECT ar.parent_account
      FROM Account_Relationship ar
      WHERE ar.child_account = NEW.parent_account -- Just get direct parents of the new parent - first level
    ),
    ExtendedPathToCheck(Ancestor) AS ( -- Expand path for a limited number of iterations (e.g., 10)
      SELECT Ancestor FROM PathToCheck
      UNION ALL
      SELECT ar.parent_account
      FROM Account_Relationship ar
      JOIN ExtendedPathToCheck ept ON ar.child_account = ept.Ancestor
      WHERE ar.parent_account IS NOT NULL LIMIT 10 -- Limit iterations to prevent runaway query
    )
    SELECT
        CASE
            WHEN EXISTS (SELECT 1 FROM ExtendedPathToCheck WHERE Ancestor = NEW.child_account)
            THEN
                RAISE(FAIL, 'Cycle detected in Account Relationship (Iterative No Temp Table Check): Cannot create relationship where child would become an ancestor.')
        END;
END;
